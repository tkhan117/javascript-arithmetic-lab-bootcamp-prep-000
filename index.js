function add(a, b)
{
  return a + b
}

function subtract(a, b)
{
  return a - b
}

function multiply(a, b)
{
  return a * b
}

function divide(a, b)
{
  return a / b
}

function inc(n)
{
var n = 1
  return n++
}

function dec(n)
{
var n = 2
  return n--
}

function makeInt(n)
{
  return parseInt(n)
}
